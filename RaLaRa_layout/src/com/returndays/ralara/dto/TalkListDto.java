package com.returndays.ralara.dto;

public class TalkListDto {

	public String USER_SEQ;
	public String TITLE;
	public String ThumbNail;
	public String COLOR;
	public String EXPIRE_DATE;
	public String ROOM_SEQ;
	public String DEL_FLAG;
	public String ROOM_USERS_COUNT;
	public String TYPE;
	
	public TalkListDto(String title, String thum, String color){
		this.TITLE = title;
		this.ThumbNail = thum;
		this.COLOR = color;
	}
	
	
	
	public TalkListDto(String user_seq, String title, String thum, String color, String expire_date, String seq, String del_flag, String user_count, String type){
		this.USER_SEQ = user_seq;
		this.TITLE = title;
		this.ThumbNail = thum;
		this.COLOR = color;
		this.EXPIRE_DATE = expire_date;
		this.ROOM_SEQ = seq;
		this.DEL_FLAG = del_flag;
		this.ROOM_USERS_COUNT = user_count;
		this.TYPE = type;
	}
}
