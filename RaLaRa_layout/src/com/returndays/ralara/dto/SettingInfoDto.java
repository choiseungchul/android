package com.returndays.ralara.dto;

public class SettingInfoDto {
	public String TITLE;
	public String CHECKBOX;
	public String RIGHT_TITLE;
	public String GUBUNSTR;
	public int ID;
	public boolean isChecked = false;
	public String UPDATE_ALERT;
	
	/**
	 * 
	 * 타이틀,
	 * 체크박스 여부,
	 * 수량 정보,
	 * 구분 선 택스트
	 * */
	public SettingInfoDto(String title, String checkbox, String right_title, String gubun_title, int id, boolean isChecked, String update_alert){
		this.TITLE = title;
		this.CHECKBOX = checkbox;
		this.RIGHT_TITLE = right_title;
		this.GUBUNSTR = gubun_title;
		this.ID = id;
		this.isChecked = isChecked;
		this.UPDATE_ALERT = update_alert;
	}
}
